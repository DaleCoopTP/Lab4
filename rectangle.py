def area(length, width):
    """
    Возвращает площадь прямоугольника.

    Параметры:
        lenght (int) - первое десятичное число(длина)
        width (int) - второе десятичное число(ширина)

    Возвращаемое значение:
        Площадь прямоугольника - lenght * width (int).
    """
    return length * width